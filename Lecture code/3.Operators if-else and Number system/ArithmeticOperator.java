public class ArithmeticOperator{
    public static void main(String[] args) {
        int a=10;
        int b=6;
        System.out.println("a+b= "+(a+b));
        System.out.println("a-b= "+(a-b));
        System.out.println("a*b= "+(a*b));
        System.out.println("a/b= "+(a/b));
        System.out.println("a%b= "+(a%b));
        double c=10.0;
        double d=6.0;
        System.out.println("c/d= "+(c/d));
        System.out.println("c%d= "+(c%d));
    }
}