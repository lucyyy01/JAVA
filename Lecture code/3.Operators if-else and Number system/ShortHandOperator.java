public class ShortHandOperator {
    public static void main(String[] args) {
        int a = 10;
        a += 5; // a = a + 5
        System.out.println(a); // 15

        int b = 20;
        b -= 10; // b = b - 10
        System.out.println(b); // 10

        int c = 5;
        c *= 2; // c = c * 2
        System.out.println(c); // 10

        int d = 50;
        d /= 5; // d = d / 5
        System.out.println(d); // 10

        int e = 3;
        e %= 2; // e = e % 2
        System.out.println(e); // 1
    }
}
